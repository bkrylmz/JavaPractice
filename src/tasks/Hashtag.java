package tasks;

import java.util.Scanner;

public class Hashtag {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        for (int i = 1; i <= n ; i++) {
            System.out.print("#");
        }
        /*
        Use a `loop` to print a hashtag `#` for `n` times.
        The `n` value is how many hashtags should be on the same line, in the output.
         */



    }
}
