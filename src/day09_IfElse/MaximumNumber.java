package day09_IfElse;

public class MaximumNumber {
    public static void main(String[] args) {
        int num1 = 85698;
        int num2 = 84563;
        boolean num1IsMaximum = num1 > num2;

        if (num1IsMaximum){
            System.out.println(num1 + " is maximum number");
        }else{
            System.out.println(num2 + " is maximum number");}










    }
}
